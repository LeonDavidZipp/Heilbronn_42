#include <unistd.h>

void	ft_print_comb2(void)
{
	int		nr1;
	int		nr2;

	nr1 = 0;
	nr2 = 1;
	while (nr1 <= 98)
	{
		while (nr2 <= 99)
		{
			write(1, (char [2]){'0' + nr1 / 10, '0' + nr1 % 10}, 2);
			write(1, " ", 1);
			write(1, (char [2]){'0' + nr2 / 10, '0' + nr2 % 10}, 2);
			if (nr1 < 98)
			{
				write(1, ", ", 2);
			}
			if (nr2 == 99)
			{
				nr2 = nr1 + 1;
				nr1++;
			}
			nr2++;
		}
	}
}
